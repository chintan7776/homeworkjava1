package A.DataTypes;

public class DataTypesJava {

    public static void main(String[] args) {

        //Bytes range is -128 to 127
        byte a=127;

        System.out.println("a="+a);

        //Short range is -32768 to 32767
        short b=-32768;

        System.out.println("b="+b);

        //int range is -2147483648 to 2147483647

        int c=-1000000000;

        System.out.println("c="+c);

        //Long rang is -9223372036854775 to 9223372036854775

        long d= 9000000000000000000l;

        System.out.println("d="+d);

        //float range 3.4E-38 to 3.4E+38

        float e=231.0123f;
        System.out.println("e="+e);

        //double range is approx 1.8 * (10)308
        double f=123.12345;
        System.out.println("f="+f);

        //character
        char g='$';
        System.out.println("g="+g);

        //boolean

        boolean h= true;
        boolean i= false;

        System.out.println("h="+h);
        System.out.println("i="+i);





    }
}
